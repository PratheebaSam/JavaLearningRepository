package com.tyss.arraylist.getdatadiff;

public class Employee {
	int id;
	String employeeName;
	String designation;
	double salary;
	public Employee(int id, String employeeName, String designation, double salary) {
		super();
		this.id = id;
		this.employeeName = employeeName;
		this.designation = designation;
		this.salary = salary;
	}
	
}
